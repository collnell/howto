## git - version control software
# manages changes to a project without overwriting  
# improves collaboration, transparency, reproducibility  

## github - visual interface to git, local mgmt  
# local interaction & open sharing network  
# can work within command line or using desktop client 
# https://help.github.com/desktop/guides/getting-started-with-github-desktop/ 

## vocab  
# command line - program to input git commands (terminal on mac  
# repository - directory where projects are stored, can be local folder on computer or online host.storecode scripts, text files, images, datasets  
# version control - snapshots of time points in project's history. don't lose or overwrite work  
# commit - take the snapshot or the repository, saved checkpoint  
# branch - version of a project taken from a commit. multiple people can edit simultaneously, merge back with master branch of main directory  
# fork - copy of a repository  
# push - make local version visible online  
# pull - get up-to-date version of repositoy to local drive  

